using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class PlayerBoomerangCatchState : PlayerAbilityState {
  public PlayerBoomerangCatchState(Player player, PlayerStateMachine stateMachine, PlayerData playerData, string animationBoolName) : base(player, stateMachine, playerData, animationBoolName) {
  }

  public override void Enter() {
    base.Enter();

    stateMachine.ChangeState(player.IdleState);
  }

  public override void Exit() {
    base.Exit();
  }

  public override void LogicUpdate() {
    base.LogicUpdate();
  }

  public override void PhysicsUpdate() {
    base.PhysicsUpdate();
  }

  public override void DoChecks() {
    base.DoChecks();
  }
}